public class CharAndBoolean {
    public static void main(String[] args) {
        char letter = 'A';         // 16 bit Unicode karakter
        boolean dogru = true;  // boolean tipi, true veya false değeri alır
        boolean yanlis = false;
        boolean evet = true;
        boolean hayir = false;

        System.out.println("Harf: " + letter);
        System.out.println("Doğru mu?: " + dogru);
        System.out.println("Yanlış mı?: " + yanlis);
        System.out.println("Evet mi?: " + evet);
        System.out.println("Hayır mı?: " + hayir);



    }

}
