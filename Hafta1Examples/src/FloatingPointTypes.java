public class FloatingPointTypes {
    public static void main(String[] args) {
        float heat = 36.6F; // 32 bit, F harfi ile float tipi belirtilir
        double pi = 3.14159;    // 64 bit, double varsayılan ondalıklı sayı tipi

        System.out.println("Sıcaklık: " + heat);
        System.out.println("Pi Değeri: " + pi);
    }
}
