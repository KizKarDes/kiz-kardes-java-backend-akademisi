public class LogicalOperatorsExample {
    public static void main(String[] args) {
        boolean condition1 = (5 > 3);
        boolean condition2 = (8 < 6);

        System.out.println("condition1 && condition2: " + (condition1 && condition2)); // Ve
        System.out.println("condition1 || condition2: " + (condition1 || condition2)); // Veya
        System.out.println("!condition1: " + (!condition1));                            // DeÄŸil
    }
}
