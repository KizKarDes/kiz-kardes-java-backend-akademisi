public class AssignmentOperatorsExample {
    public static void main(String[] args) {
        int num = 10;

        num += 5;  // num = num + 5
        System.out.println("num += 5: " + num);

        num -= 3;  // num = num - 3
        System.out.println("num -= 3: " + num);

        num *= 2;  // num = num * 2
        System.out.println("num *= 2: " + num);

        num /= 4;  // num = num / 4
        System.out.println("num /= 4: " + num);

        num %= 3;  // num = num % 3
        System.out.println("num %= 3: " + num);
    }
}
